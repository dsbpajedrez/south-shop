export const categoryTitle =(id)=>{
    switch (id) {
      case '1':       
        return 'ropa'      
      case '2':       
        return 'electronico'      
      case '3':      
        return 'muebles'      
      case '4':       
        return 'zapatos'      
        case '5':       
          return 'otros'        
      default:       
        return 'zapatos'
    }
  }